uint32 ICU_Capture_Time(void)
{
	if (ICU_Cnt ==3)
	{
		ICU_Time_on= CAP2 - CAP1;
		ICU_Time_Total = Cap3 - CAP1 ; 
		/******** INT ON ICU ***********/
		SET_BIT(TIMSK,5);
		ICU_Cnt=0;
	}
	return ICU_Time_Total;
}

