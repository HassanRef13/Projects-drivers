void Ultrasonic_Trigger(void)
{
	/*********trigger *************/
	DIO_WriteChannel(PORTC_PIN6, PIN_HIGH);
	_delay_us(10);
	DIO_WriteChannel(PORTC_PIN6, PIN_LOW);
	
}

