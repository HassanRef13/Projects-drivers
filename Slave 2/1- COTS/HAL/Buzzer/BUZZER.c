/******************************************************************************
* \Syntax          : void BUZZER_SET_VALUE(BUZZER_VALUE_t BUZZER_Value)
* \Description     : BUZZER ON/OFF
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : void  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void BUZZER_SET_VALUE(BUZZER_VALUE_t BUZZER_Value)
{
	DIO_WriteChannel(BUZZER,BUZZER_Value);
}
/******************************************************************************
* \Syntax          : void BUZZER_toggle(void)
* \Description     : BUZZER toggle
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : void  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void BUZZER_toggle(void)
{
	DIO_FlipChannel(BUZZER);
}

