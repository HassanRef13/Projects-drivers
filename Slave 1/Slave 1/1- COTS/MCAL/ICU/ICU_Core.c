uint32 ICU_Capture_Time(void)
{
	static uint32 ICU_time ;
	
	if (ICU_Cnt ==2)
	{
		ICU_time= (CAP2 - CAP1) *64;
		ICU_Cnt=0;
		/******** INT ON ICU ***********/
		SET_BIT(TIMSK,5);
		
	}
	
	return ICU_time;
}

